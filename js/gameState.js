// このファイルは「グローバルな状態置き場」です。
//
// ゲームには多くのファイルがありますが、「今パーティは何人いるか」
// 「所持金はいくらか」などの情報は全ファイルから参照できる必要があります。
// そのため、このファイルで一元管理して export（公開）しています。
//
// 主な内容：
//   DB     = テキストファイルから読み込んだゲームデータ（マスタデータ）
//   Party  = プレイヤーのパーティ情報（メンバー・所持金・位置など）
//   Screens= 画面切替システム
// ============================================================

// ---------------------------------------------------
// DB: ゲームのマスターデータ置き場
// ---------------------------------------------------
// jobs.txt, weapons.txt などから読み込んだデータがここに格納されます。
// キー（id）で素早く検索できるよう、オブジェクト形式で保存します。
// 例: DB.jobs['warrior'] → 戦士の職業データ
// 例: DB.monsters['kobold'] → コボルトの敵データ
export const DB = {
  jobs: {},      // 職業データ { id: {name, hp_die, str, ...} }
  weapons: {},   // 武器データ { id: {name, dmg_dice, value, ...} }
  armors: {},    // 防具データ { id: {name, ac_bonus, value, ...} }
  items: {},     // アイテムデータ { id: {name, type, effect, ...} }
  spells: {},    // 呪文データ { id: {name, school, level, power, ...} }
  monsters: {},  // モンスターデータ { id: {name, hp_dice, ac, attacks, ...} }
  maps: {},      // マップデータ { フロア番号: 2D配列 }  例: { 1: [[]], 2: [[]] }
};

// ---------------------------------------------------
// Party: プレイヤーのパーティ情報（プレイ中に変化するデータ）
// ---------------------------------------------------
// このオブジェクトはゲーム中にどんどん変化します。
// 所持金が増えれば gold が増え、メンバーを追加すれば members が増えます。
export const Party = {
  roster:  [],         // 冒険者名簿：酒場に登録された全キャラクター一覧
  members: [],         // 現在のパーティ：実際に冒険に出るメンバー（最大6人）
  gold: 500,           // 所持金（ゴールド）。初期値は500G
  floor: 1,            // 現在いるダンジョンのフロア番号（1 = 最上階）
  pos: { x: 1, z: 1 }, // ダンジョン内のマス座標（x=列, z=行）
  dir: 0,              // 向いている方向 0=北 1=東 2=南 3=西
};

// ---------------------------------------------------
// 画面切替システム
// ---------------------------------------------------
// ゲームには複数の画面（タイトル・街・酒場など）があります。
// ここでは「どの画面が登録されているか」と「今どの画面か」を管理します。

/**
 * 登録された画面ハンドラの一覧
 * 構造: { 画面名: { show(), hide() } }
 * 例: Screens['town'] = { show: () => {...}, hide: () => {...} }
 */
export const Screens = {};

/** 現在表示中の画面名を記憶する変数（null = まだ何も表示していない）*/
let _currentScreen = null;

/**
 * 画面を登録する
 * @param {string} id       - 画面を識別する名前（例: 'title', 'town'）
 * @param {object} handlers - { show(), hide() } を持つオブジェクト
 *
 * 使い方: registerScreen('town', initTownUI())
 * → 'town' という名前で show/hide を呼べるようになる
 */
export function registerScreen(id, handlers) {
  Screens[id] = handlers;
}

/**
 * 指定した画面に切り替える
 * @param {string} id - 表示したい画面名
 *
 * 仕組み：
 *   1. 今表示中の画面があれば hide() で隠す
 *   2. 新しい画面の show() を呼んで表示する
 *   3. _currentScreen を新しい画面名に更新する
 *
 * 使い方: showScreen('tavern') → 酒場画面に切り替わる
 */
export function showScreen(id) {
  // 現在の画面を隠す（?.は「存在すれば実行」という安全呼び出し）
  if (_currentScreen && Screens[_currentScreen]) Screens[_currentScreen].hide?.();
  _currentScreen = id;
  // 新しい画面を表示する
  if (Screens[id]) Screens[id].show?.();
}

/**
 * 現在表示中の画面名を返す
 * @returns {string|null}
 */
export function currentScreen() { return _currentScreen; }
